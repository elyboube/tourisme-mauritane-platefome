        v_cursor SYS_REFCURSOR;
    BEGIN
        OPEN v_cursor FOR
            SELECT d.id, d.nom, d.note_moyenne, d.nb_commentaires,
                   COUNT(r.id) AS nb_reservations
            FROM DESTINATION d
